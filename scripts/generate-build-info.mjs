/**
 * Invoked from `yarn build` / `npm run build` before `nest build`.
 * Writes ISO timestamp and optional git short SHA into `src/build-info.generated.ts`.
 */
import { execSync } from 'node:child_process';
import { writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = join(__dirname, '..');
const outFile = join(root, 'src', 'build-info.generated.ts');
const iso = new Date().toISOString();

let gitCommit = '';
try {
  gitCommit = execSync('git rev-parse --short HEAD', {
    cwd: root,
    encoding: 'utf8',
  }).trim();
} catch {
  // Sin git o fuera de repo: se deja vacío; CI puede inyectar GIT_COMMIT en env.
}

writeFileSync(
  outFile,
  `// Auto-generated by scripts/generate-build-info.mjs — do not edit.
export const BUILD_ISO_DATE = '${iso}';
export const GIT_COMMIT = ${JSON.stringify(gitCommit)};
`,
  'utf8',
);
